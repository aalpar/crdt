// Helpers for ORMap[string, *DotSet] — a map of sets.

func newSetMap(id dotcontext.ReplicaID) *ORMap[string, *dotcontext.DotSet] {
	return New[string, *dotcontext.DotSet](id, dotcontext.JoinDotSetStore, dotcontext.NewDotSet)
}

func addDot(id dotcontext.ReplicaID, ctx *dotcontext.CausalContext, v *dotcontext.DotSet, delta *dotcontext.DotSet) {
	d := ctx.Next(id)
	v.Add(d)
	delta.Add(d)
}

func TestBasicOps(t *testing.T) {
	c := qt.New(t)

	c.Run("NewEmpty", func(c *qt.C) {
		m := newSetMap("a")
		c.Assert(m.Len(), qt.Equals, 0)
		c.Assert(m.Has("x"), qt.IsFalse)
		c.Assert(m.Keys(), qt.HasLen, 0)
	})

	c.Run("ApplyCreatesKey", func(c *qt.C) {
		m := newSetMap("a")
		m.Apply("x", addDot)

		c.Assert(m.Has("x"), qt.IsTrue)
		c.Assert(m.Len(), qt.Equals, 1)
		v, ok := m.Get("x")
		c.Assert(ok, qt.IsTrue)
		c.Assert(v.Len(), qt.Equals, 1)
	})

	c.Run("ApplyMultipleKeys", func(c *qt.C) {
		m := newSetMap("a")
		m.Apply("x", addDot)
		m.Apply("y", addDot)
		m.Apply("z", addDot)

		keys := m.Keys()
		slices.Sort(keys)
		c.Assert(keys, qt.DeepEquals, []string{"x", "y", "z"})
	})

	c.Run("ApplyAccumulatesDots", func(c *qt.C) {
		m := newSetMap("a")
		m.Apply("x", addDot)
		m.Apply("x", addDot)

		v, _ := m.Get("x")
		c.Assert(v.Len(), qt.Equals, 2)
	})

	c.Run("Remove", func(c *qt.C) {
		m := newSetMap("a")
		m.Apply("x", addDot)
		m.Apply("y", addDot)
		m.Remove("x")

		c.Assert(m.Has("x"), qt.IsFalse)
		c.Assert(m.Has("y"), qt.IsTrue)
	})

	c.Run("RemoveAbsent", func(c *qt.C) {
		m := newSetMap("a")
		m.Remove("ghost") // should not panic
		c.Assert(m.Len(), qt.Equals, 0)
	})

	c.Run("RemoveThenReApply", func(c *qt.C) {
		m := newSetMap("a")
		m.Apply("x", addDot)
		m.Remove("x")
		c.Assert(m.Has("x"), qt.IsFalse)

		m.Apply("x", addDot)
		c.Assert(m.Has("x"), qt.IsTrue)
		c.Assert(m.Len(), qt.Equals, 1)
	})

	c.Run("Context", func(c *qt.C) {
		a := newSetMap("a")
		a.Apply("x", addDot)

		ctx := a.Context()
		c.Assert(ctx, qt.Not(qt.IsNil))
		// The context should contain the dot generated by Apply.
		c.Assert(len(ctx.ReplicaIDs()), qt.Equals, 1)
		c.Assert(ctx.ReplicaIDs()[0], qt.Equals, dotcontext.ReplicaID("a"))
	})
}

